def rmse(y_true, y_pred):
    y_true = np.asarray(y_true).reshape(-1)
    y_pred = np.asarray(y_pred).reshape(-1)
    return float(np.sqrt(mean_squared_error(y_true, y_pred)))


def to_float32(x):
    return np.asarray(x, dtype=np.float32)


def nan_clean(a):
    return np.nan_to_num(np.asarray(a), nan=0.0, posinf=0.0, neginf=0.0)


def fig_hist(series_list, labels, title, bins=60):
    fig = plt.figure(figsize=(10, 4))
    for s, lab in zip(series_list, labels):
        plt.hist(s, bins=bins, alpha=0.6, label=lab)
    plt.title(title)
    plt.legend()
    return fig


def fig_scatter(x, y, title, xlabel, ylabel):
    fig = plt.figure(figsize=(6, 6))
    plt.scatter(x, y, s=6, alpha=0.4)
    mn = float(min(np.min(x), np.min(y)))
    mx = float(max(np.max(x), np.max(y)))
    plt.plot([mn, mx], [mn, mx])
    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel(ylabel)
    return fig


def fig_residuals(pred, resid, title):
    fig = plt.figure(figsize=(10, 4))
    plt.scatter(pred, resid, s=6, alpha=0.4)
    plt.axhline(0.0)
    plt.title(title)
    plt.xlabel("prediction")
    plt.ylabel("residual (y - pred)")
    return fig


def make_zip(files_dict):
    """
    files_dict: {filename: bytes}
    """
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        for fname, content in files_dict.items():
            zf.writestr(fname, content)
    buf.seek(0)
    return buf.getvalue()


