def load_from_bundle_csv(bundle_df):
    """
    bundle_df must contain a 'section' column with:
      - train_rows
      - test_rows
      - importance_gain (optional)
      - importance_permutation (optional)
    """
    if "section" not in bundle_df.columns:
        raise ValueError("Le CSV bundle doit contenir une colonne 'section'.")

    train_rows = bundle_df[bundle_df["section"] == "train_rows"].copy()
    test_rows = bundle_df[bundle_df["section"] == "test_rows"].copy()

    # Required cols
    for c in [ID_COL, LOC_COL, "y", "oof"]:
        if c not in train_rows.columns:
            raise ValueError(f"train_rows doit contenir la colonne '{c}'.")

    if "test_pred" not in test_rows.columns:
        raise ValueError("test_rows doit contenir la colonne 'test_pred'.")

    # Cast
    y = train_rows["y"].astype(np.float32).values
    oof = train_rows["oof"].astype(np.float32).values
    test_pred = test_rows["test_pred"].astype(np.float32).values

    train_keys = train_rows[[ID_COL, LOC_COL]].copy()
    test_keys = test_rows[[ID_COL, LOC_COL]].copy()

    # Tab columns = everything except known non-feature columns
    non_feature = set(["section", ID_COL, LOC_COL, "y", "oof", "test_pred",
                       "feature", "importance_gain", "perm_mean", "perm_std"])
    tab_cols = [c for c in train_rows.columns if c not in non_feature]

    X_tab_train = train_rows[tab_cols].astype(np.float32).values if len(tab_cols) > 0 else None
    X_tab_test = test_rows[tab_cols].astype(np.float32).values if len(tab_cols) > 0 else None

    tab_feature_names = tab_cols

    # Optional importances
    imp_gain = bundle_df[bundle_df["section"] == "importance_gain"].copy()
    imp_perm = bundle_df[bundle_df["section"] == "importance_permutation"].copy()

    imp_gain_df = None
    imp_perm_df = None
    if "feature" in imp_gain.columns and "importance_gain" in imp_gain.columns and len(imp_gain) > 0:
        imp_gain_df = imp_gain[["feature", "importance_gain"]].dropna()

    if "feature" in imp_perm.columns and "perm_mean" in imp_perm.columns and len(imp_perm) > 0:
        cols = ["feature", "perm_mean"] + (["perm_std"] if "perm_std" in imp_perm.columns else [])
        imp_perm_df = imp_perm[cols].dropna()

    return {
        "y": y,
        "oof": oof,
        "test_pred": test_pred,
        "train_keys": train_keys,
        "test_keys": test_keys,
        "X_tab_train": X_tab_train,
        "X_tab_test": X_tab_test,
        "tab_feature_names": tab_feature_names,
        "imp_gain_df": imp_gain_df,
        "imp_perm_df": imp_perm_df,
    }


def load_from_separate_files(files):
    """
    files: dict of uploaded file objects.
    Expected:
      - oof.npy, y.npy, test_pred.npy
      - train_keys.csv, test_keys.csv
      - X_tab_train.npy, X_tab_test.npy (optional but needed for LightGBM)
      - tab_feature_names.csv (optional but recommended)
      - lgb_importance_gain.csv / permutation csv (optional)
    """
    def read_npy(uploaded):
        return np.load(uploaded)

    def read_csv(uploaded):
        return pd.read_csv(uploaded)

    y = read_npy(files["y.npy"]).astype(np.float32).reshape(-1)
    oof = read_npy(files["oof.npy"]).astype(np.float32).reshape(-1)
    test_pred = read_npy(files["test_pred.npy"]).astype(np.float32).reshape(-1)

    train_keys = read_csv(files["train_keys.csv"])
    test_keys = read_csv(files["test_keys.csv"])

    X_tab_train = None
    X_tab_test = None
    if "X_tab_train.npy" in files and "X_tab_test.npy" in files:
        X_tab_train = nan_clean(read_npy(files["X_tab_train.npy"]).astype(np.float32))
        X_tab_test = nan_clean(read_npy(files["X_tab_test.npy"]).astype(np.float32))

    tab_feature_names = None
    if "tab_feature_names.csv" in files:
        tab_feature_names = pd.read_csv(files["tab_feature_names.csv"], header=None)[0].tolist()

    imp_gain_df = None
    imp_perm_df = None
    if "lgb_importance_gain.csv" in files:
        imp_gain_df = read_csv(files["lgb_importance_gain.csv"])
    if "lgb_importance_permutation.csv" in files:
        imp_perm_df = read_csv(files["lgb_importance_permutation.csv"])

    return {
        "y": y,
        "oof": oof,
        "test_pred": test_pred,
        "train_keys": train_keys,
        "test_keys": test_keys,
        "X_tab_train": X_tab_train,
        "X_tab_test": X_tab_test,
        "tab_feature_names": tab_feature_names,
        "imp_gain_df": imp_gain_df,
        "imp_perm_df": imp_perm_df,
    }


